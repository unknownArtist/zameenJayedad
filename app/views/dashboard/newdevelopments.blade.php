<p>Golf courses are usually appreciated for the diversity in terrain they are able to adapt to the golfing fields. Bahria Golf City is rare, and un-rivaled in Pakistan, in this that even internationally one does not find more than a few resort converted golf courses.</p>
<p>Bahria Golf City in fact brings two of the rarities together in one project; a hill resort and a international standard golf course. Life here at the Bahria Golf City is all about being on vacation every day. No matter your retired, a high energy businessman or just vacationing, Bahria Golf City has all the attractions, amenities and reasons for you to live your life to the fullest.</p>
<p>Jewel of the project will be the 5 star Sheraton Golf City Resort Hotel. Sheraton Islamabad Golf City Resort will be the first Internationally branded golf resort in Pakistan and will comprise a 372-room luxury hotel that includes serviced villas. It will also feature 3,200m2 of state-of-the-art meeting space and a choice of five restaurants and bars, as well as a high-tech gym, fully-serviced spa and a number of beautifully designed outdoor swimming pools. Guests will of course have full access to the onsite 18-hole golf course and Sheraton Golf & Country Club. </p>
<p>The club house here affords the lifestyle of a true golfer and a community lover. Expect only the very best and the very contemporary of facilities and services making every day a new unforgettable one. All sports facilitating services for both ladies and gents, including a fully equipped Pro-Shop refreshes your golfing experience. For the cuisine connoisseurs a full range of indoors and outdoors arrangements sprinkle that right bit of the ambiance that lifts your dining moments. The lavishly laid out fire place recreates the aura of a bon-fire with your friends and family for a sumptuous dinner or a party.</p>
