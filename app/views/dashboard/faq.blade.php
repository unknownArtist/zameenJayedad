<h3>What is a Buyer's Representative?</h3>
<p>In every real estate transaction, there is a buyer and a seller. A buyer's representative is a licensed real estate sales professional that specializes in representing the best interests of the buyer.
Prospective buyers sometimes make the mistake of assuming they will save themselves time and money by dealing directly with the listing representative, not realizing that he or she represents the seller. A representative cannot truly represent both the seller and the buyer in any transaction, since each person has competing interests.</p>
<h3>Does using a Buyer's Agent increase the cost of purchase?</h3>
<p>Use of a Buyer's Representative does not increase the amount of commission in almost all cases. The commission in a real estate transaction can vary from one brokerage to another and from one sale to another, but that commission is divided between the selling agency, and the buying agency.</p>
<h3>Do I need to give a deposit in my offer?</h3>
<p>A deposit is not legally required when making an offer on a property, but it can be a good idea. Factors to think about when considering a deposit are how hot the market is, how much you are offering and most importantly, how serious you are about buying the property; this is what the deposit is intended to prove.
If your offer is on the lower side, a deposit can give you some leverage, convincing the seller to accept before you are outbid. Because the funds are secured in a trust account for 10 business days, you cannot use it to make an offer on something else you see. It is also a good idea to put a number of subjects for removal on the offer, detailing what conditions must be met before the sale goes through. This will eliminate any surprises before you buy the home. Remember, once both parties have signed off on the offer, you have entered a legally binding contract.</p>
<h3>What is a Foreclosure Sale?</h3>
<p>When home owner defaults on payments and cannot come to an agreement with the grantor, the lending financial institution may call foreclosure proceedings in three to four months so that they can recover funds loaned to the borrower. Typically, the borrower will have been in arrears for 90 days.
Should the borrower be unable to either sell the home or bring the mortgage up to date, the lender will submit an application for the conduct of sale, which, if approved will authorize the property s sale.</p>

